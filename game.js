//Cria a pergunta aleatoriamente e a exclui da lista de perguntas 
function criaPergunta (){
    timer = 90;
    nivel = verificaNivel();
    aleatorio = random(nivel);
    var p = nivel[aleatorio];
    pergunta.innerHTML = "<h1>" + p.pergunta + "</h1>";
    escolha1.innerHTML = "<p>" + p.escolha1 + "</p>";
    escolha2.innerHTML = "<p>" + p.escolha2 + "</p>";
    escolha3.innerHTML = "<p>" + p.escolha3 + "</p>";
    escolha4.innerHTML = "<p>" + p.escolha4 + "</p>";
    respostaAtual = p.resposta;
    nivel.splice(aleatorio, 1);
}

//randomizar as perguntas DISPONIVEIS
function random(nivel){
    let aleatorio = Math.floor(Math.random() * nivel.length);
    return aleatorio;
}

//Verificar o nivel da pergunta, e devolve o array que será utilizado
function verificaNivel(){
    if(cont < 5){
        nivel = perguntas_Nvl1;
    }
    else if(cont < 10){
        nivel = perguntas_Nvl2;
    }
    else if(cont < 15){
        nivel = perguntas_Nvl3;
    }
    else if(cont === 15){
        alert("PRÓXIMA PERGUNTA VALE 1 MILHÃO");
        nivel = perguntas_Nvl4;
    }
    else{
        alert("PARABÉNS, VOCÊ ACABA DE SE TORNAR UM MILIONÁRIO!!!🎉🎉🎉");
        return window.location.assign("index.html");
    }
    return nivel;
}

//Verificar se a resposta esta correta/adiciona mais um ao contador nesse caso
function verificaResposta(escolha){
    //Pergunta "ESTA CERTO DISSO?"
    if(escolha === respostaAtual){
        cont++;
        premioAcumulado = premios[cont];
        pontos.innerHTML = "Você possui R$"+ premioAcumulado + ",00";
        erro.innerHTML = "Se errar ganha R$"+ premioAcumulado/2 + ",00"; 
        acerto.innerHTML = "Pergunta valendo R$"+ premios[cont+1] + ",00";
        criaPergunta();
    }   
    else{
        gameOverLose();
    }
}

//Pula a questão e a retira do array
function pular(){
    if(contPulos < 3){
        contPulos++;
        pulou.innerHTML = "Pular" + contPulos + "/3";
        criaPergunta();
    }
    else{
        alert('PULO ESGOTADO');
    }
}

function gameOverWin(){
    alert("Parabéns, você ganhou R$" + premioAcumulado + ",00");
    return window.location.assign("index.html");
}

function gameOverLose(){
    alert("Oh não, parece que a respota está errada, mas pelo menos você levou R$" + premioAcumulado/2 + ",00");
    return window.location.assign("index.html");
}

